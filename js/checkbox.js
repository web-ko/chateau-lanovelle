$("input[name='check[]']").on('click', function() {
	var val = $(this).val();
	var parent = $(this);
  $("input[value='"+val+"']").each(function() {
  	$(this).not(parent).attr('disabled', parent.is(':checked'));
  });
})

function uncheck() {
     $(':checkbox:checked').prop('checked',false);
 }